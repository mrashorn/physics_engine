namespace cyclone {

	/**
	 * Defines a real number precision. Cyclone can be compiled in
	 * single or double precision versions. By default single precision
	 * is provided. 
	 *
	 * A typedef allows us to easily swap the real type from float
	 * to double right here in one spot, and that will disperse 
	 * throughout the project.
	 */

	typedef float real; // defines the type real as a float

}
